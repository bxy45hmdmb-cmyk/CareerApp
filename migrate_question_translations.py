# Delete existing and re-insert
c.execute("DELETE FROM question_translations WHERE lang='ru'")

inserted = 0
for q_id, (text, options) in ru_data.items():
    c.execute('SELECT id FROM questions WHERE id=?', (q_id,))
    if not c.fetchone():
        print(f'  Question {q_id} not found, skipping')
        continue
    c.execute(
        '''INSERT INTO question_translations (question_id, lang, text, options)
           VALUES (?, 'ru', ?, ?)''',
        (q_id, text, json.dumps(options, ensure_ascii=False))
    )
    inserted += 1

conn.commit()
conn.close()
print(f'Inserted {inserted} Russian question translations')
print('Done!')
